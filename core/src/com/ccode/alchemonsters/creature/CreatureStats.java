package com.ccode.alchemonsters.creature;

public class CreatureStats {

	public byte vitae;
	public byte focus;
	public byte magicATK;
	public byte magicDEF;
	public byte physATK;
	public byte physDEF;
	public byte speed;
	public byte crit;
	
	public CreatureStats(byte vitae, byte focus, byte magicATK, byte magicDEF, byte physATK, byte physDEF, byte speed, byte crit) {
		this.vitae = vitae;
		this.focus = focus;
		this.magicATK = magicATK;
		this.magicDEF = magicDEF;
		this.physATK = physATK;
		this.physDEF = physDEF;
		this.speed = speed;
		this.crit = crit;
	}
	
	
	
}
